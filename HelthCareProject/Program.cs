using HelthCareProject.Models;
using Microsoft.EntityFrameworkCore;
using System.ComponentModel;

namespace HelthCareProject
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.
            builder.Services.AddControllersWithViews();
            #region Connect To MySQL DB
            var ConnectionString = builder.Configuration.GetConnectionString("mySql");
            builder.Services.AddDbContext<helthcare_systemContext>(options =>
                options.UseMySql(ConnectionString, ServerVersion.Parse("8.0.41-mysql")));
            #endregion

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (!app.Environment.IsDevelopment())
            {
                app.UseExceptionHandler("/Home/Error");
                // The default HSTS value is 30 days. You may want to change this for production scenarios, see https://aka.ms/aspnetcore-hsts.
                app.UseHsts();
            }

            app.UseHttpsRedirection();
            app.UseRouting();

            app.UseAuthorization();

            app.MapStaticAssets();
            app.MapControllerRoute(
                name: "default",
                pattern: "{controller=Home}/{action=Index}/{id?}")
                .WithStaticAssets();

            app.Run();
        }
    }
}
